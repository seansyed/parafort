import mongoose from 'mongoose';

class MongooseConnection {
  private static instance: MongooseConnection;
  private isConnected: boolean = false;

  private constructor() {}

  public static getInstance(): MongooseConnection {
    if (!MongooseConnection.instance) {
      MongooseConnection.instance = new MongooseConnection();
    }
    return MongooseConnection.instance;
  }

  public async connect(): Promise<void> {
    if (this.isConnected) {
      return;
    }

    try {
      const mongoUri = process.env.MONGODB_URI || process.env.DATABASE_URL;
      
      if (!mongoUri) {
        throw new Error('MongoDB connection string not found in environment variables');
      }

      // Configure mongoose options for better performance and reliability
      const options = {
        maxPoolSize: 10,
        serverSelectionTimeoutMS: 5000,
        socketTimeoutMS: 45000,
        bufferCommands: false,
        bufferMaxEntries: 0
      };

      await mongoose.connect(mongoUri, options);
      
      this.isConnected = true;
      console.log('‚úÖ MongoDB connected successfully');

      // Handle connection events
      mongoose.connection.on('error', (error) => {
        console.error('‚ùå MongoDB connection error:', error);
        this.isConnected = false;
      });

      mongoose.connection.on('disconnected', () => {
        console.log('‚ö†Ô∏è MongoDB disconnected');
        this.isConnected = false;
      });

      mongoose.connection.on('reconnected', () => {
        console.log('üîÑ MongoDB reconnected');
        this.isConnected = true;
      });

    } catch (error) {
      console.error('‚ùå Failed to connect to MongoDB:', error);
      this.isConnected = false;
      throw error;
    }
  }

  public async disconnect(): Promise<void> {
    if (!this.isConnected) {
      return;
    }

    try {
      await mongoose.disconnect();
      this.isConnected = false;
      console.log('üì¥ MongoDB disconnected successfully');
    } catch (error) {
      console.error('‚ùå Error disconnecting from MongoDB:', error);
      throw error;
    }
  }

  public getConnectionStatus(): boolean {
    return this.isConnected && mongoose.connection.readyState === 1;
  }
}

export const mongoDb = MongooseConnection.getInstance();